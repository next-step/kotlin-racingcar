package week1

class Calculator {
    fun plus(num1: Float, num2: Float): Float {
        return num1 + num2
    }

    fun minus(num1: Float, num2: Float): Float {
        return num1 - num2
    }

    fun multiply(num1: Float, num2: Float): Float {
        return num1 * num2
    }

    fun divide(num1: Float, num2: Float): Float {
        return (num1 / num2)
    }
}
